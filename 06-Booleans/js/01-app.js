// crear y comparar "booleans"
// los booleans solo pueden tener 2 valores ya sea true or false
const boolean1 = true;
const boolean2 = false;

console.log(boolean1);
// nos devuelve "true"
console.log(boolean2);
// nos devuelve "false"

// usar "typeOf" para saber que tipo de dato es
console.log(typeof boolean2);
// nos devuelve "boolean" porque efectivamente si es une boolean

 


