// operador mayor que y menor que
const numero1 = 20;
const numero2 = "20";
const numero3 = 30;


// operador mayor a...
console.log(numero1 > numero3);
// nos devuelve "false" porque 20 no es mayor que 30
console.log(numero3 > numero1);
// nos devuelve "true" porque 30 si es mayor que 20

// operador menor a...
console.log(numero1 < numero3);
// nos devuelve "true" porque 20 si es menor que 30
console.log(numero3 < numero1);
// nos devuelve "false" porque 30 no es menor que 20
