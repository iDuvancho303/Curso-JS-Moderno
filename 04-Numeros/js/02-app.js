// operacion en Javascript
const numero1 = 20;
const numero2 = 30;

let resultado;

// suma
resultado = numero1 + numero2;
console.log(resultado);
// nos devuelve 50

// resta
resultado = numero2 - numero1;
console.log(resultado);
// nos devuelve 10

// multiplicacion
resultado = numero1 * numero2;
console.log(resultado);
// me devuelve 600

// division
resultado = numero2 / numero1;
console.log(resultado);
// me devuelve 1.5

// modulo
resultado = numero2 % numero1;
console.log(resultado);
//me devuelve 10





