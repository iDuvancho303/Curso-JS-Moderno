// el orden de las operaciones
// es igual que en la escuela multiplicaciones y divisiones primero
// y despues van las sumas y las restas
let resultado;

resultado = 20 + 20 * 30;
// nos devuelve 620

// si quieres que se ejecute algo primero lo pones en parentesis
resultado = (20 + 20) * 30;
// nos devuelve 1200

// 20% de descuento en tu carrito de compras
