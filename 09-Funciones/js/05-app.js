// Parametros y Argumentos en funciones
function sumar(a, b) {  // "a" y "b" son parametros
    console.log(a + b);
}

sumar(2, 3) // estos ya son argumentos aqui le tenemos que dar algun valor a los argumentos
// nos devuelve 5

sumar(200, 184);
// nos devuelve 384 



function saludar(nombre, apellido) {
    console.log(`Hola ${nombre} ${apellido}`);
}

saludar('Duvan', 'Flores');
// nos devuelve "Hola Duvan Flores"


